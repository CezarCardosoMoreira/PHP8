<?php

//O valor `null` em PHP representa a ausência de um valor, ou seja,
// é uma variável sem nenhum valor atribuído.
// Quando uma variável é inicializada como `null`, ela está essencialmente vazia.
//- **Definição:** `null` significa literalmente "nada" ou "vazio".
//- **Variável sem valor:** Quando uma variável foi declarada,
// mas ainda não recebeu nenhum valor, ela é considerada `null`.

echo null;

$a = null;
